package com.star.sud.resource;

public class HelloWorld {

	private String message;

	public HelloWorld() {
		super();
	}

	/**
	 * @param message
	 */
	public HelloWorld(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
